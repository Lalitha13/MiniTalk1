package com.example.lalithanjali.minitalk;

public class Edu {
    private String S1;
    private String S2;
    private String S3;
    private String S4;

    public Edu() {
    }

    public Edu(String s1, String s2, String s3, String s4) {
        S1 = s1;
        S2 = s2;
        S3 = s3;
        S4 = s4;
    }

    public String getS4() {
        return S4;
    }

    public void setS4(String s4) {
        S4 = s4;
    }

    public String getS1() {
        return S1;
    }

    public void setS1(String s1) {
        S1 = s1;
    }

    public String getS2() {
        return S2;
    }

    public void setS2(String s2) {
        S2 = s2;
    }

    public String getS3() {
        return S3;
    }

    public void setS3(String s3) {
        S3 = s3;
    }
}
